<?php

namespace PAGE\DemoBundle\Entity;

use Doctrine\ORM\Mapping as ORM;


/**
 * DatosInstitucion
 *
 * @ORM\Table()
 * @ORM\Entity()
 */
class DatosInstitucion
{
    /**
     * @var string
     *
     * @ORM\Column(name="id", type="string", length=25)
     * @ORM\Id
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="rbd", type="string", length=255,nullable=true)
     */
    private $rbd;

    /**
     * @var string
     *
     * @ORM\Column(name="rut", type="string", length=255,nullable=true)
     */
    private $rut;

    /**
     * @var string
     *
     * @ORM\Column(name="address", type="string", length=255,nullable=true)
     */
    private $address;

    /**
     * @var string
     *
     * @ORM\Column(name="fax", type="string", length=255,nullable=true)
     */
    private $fax;

    /**
     * @var string
     *
     * @ORM\Column(name="rep", type="string", length=255,nullable=true)
     */
    private $rep;

    /**
     * @var string
     *
     * @ORM\Column(name="jornada", type="string", length=255, nullable=true)
     */
    private $jornada;

    /**
     * @var string
     *
     * @ORM\Column(name="modalidad", type="string", length=255, nullable=true)
     */
    private $modalidad;

    /**
     * @var string
     *
     * @ORM\Column(name="regimen", type="string", length=255, nullable=true)
     */
    private $regimen;

    /**
     * @var datetime
     *
     * @ORM\Column(name="fecha_inicio", type="datetime", nullable=true)
     */
    private $fecha_inicio;

    /**
     * @var datetime
     *
     * @ORM\Column(name="fecha_fin", type="datetime", nullable=true)
     */
    private $fecha_fin;


    /**
     * Set id
     *
     * @param string $id
     * @return DatosInstitucion
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get id
     *
     * @return string 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set rbd
     *
     * @param string $rbd
     * @return DatosInstitucion
     */
    public function setRbd($rbd)
    {
        $this->rbd = $rbd;

        return $this;
    }

    /**
     * Get rbd
     *
     * @return string 
     */
    public function getRbd()
    {
        return $this->rbd;
    }

    /**
     * Set rut
     *
     * @param string $rut
     * @return DatosInstitucion
     */
    public function setRut($rut)
    {
        $this->rut = $rut;

        return $this;
    }

    /**
     * Get rut
     *
     * @return string 
     */
    public function getRut()
    {
        return $this->rut;
    }

    /**
     * Set address
     *
     * @param string $address
     * @return DatosInstitucion
     */
    public function setAddress($address)
    {
        $this->address = $address;

        return $this;
    }

    /**
     * Get address
     *
     * @return string 
     */
    public function getAddress()
    {
        return $this->address;
    }

    /**
     * Set fax
     *
     * @param string $fax
     * @return DatosInstitucion
     */
    public function setFax($fax)
    {
        $this->fax = $fax;

        return $this;
    }

    /**
     * Get fax
     *
     * @return string 
     */
    public function getFax()
    {
        return $this->fax;
    }

    /**
     * Set jornada
     *
     * @param string $jornada
     * @return DatosInstitucion
     */
    public function setJornada($jornada)
    {
        $this->jornada = $jornada;

        return $this;
    }

    /**
     * Get jornada
     *
     * @return string 
     */
    public function getJornada()
    {
        return $this->jornada;
    }

    /**
     * Set modalidad
     *
     * @param string $modalidad
     * @return DatosInstitucion
     */
    public function setModalidad($modalidad)
    {
        $this->modalidad = $modalidad;

        return $this;
    }

    /**
     * Get modalidad
     *
     * @return string 
     */
    public function getModalidad()
    {
        return $this->modalidad;
    }

    /**
     * Set regimen
     *
     * @param string $regimen
     * @return DatosInstitucion
     */
    public function setRegimen($regimen)
    {
        $this->regimen = $regimen;

        return $this;
    }

    /**
     * Get regimen
     *
     * @return string 
     */
    public function getRegimen()
    {
        return $this->regimen;
    }

    /**
     * Set fecha_inicio
     *
     * @param \DateTime $fechaInicio
     * @return DatosInstitucion
     */
    public function setFechaInicio($fechaInicio)
    {
        $this->fecha_inicio = $fechaInicio;

        return $this;
    }

    /**
     * Get fecha_inicio
     *
     * @return \DateTime 
     */
    public function getFechaInicio()
    {
        return $this->fecha_inicio;
    }

    /**
     * Set fecha_fin
     *
     * @param \DateTime $fechaFin
     * @return DatosInstitucion
     */
    public function setFechaFin($fechaFin)
    {
        $this->fecha_fin = $fechaFin;

        return $this;
    }

    /**
     * Get fecha_fin
     *
     * @return \DateTime 
     */
    public function getFechaFin()
    {
        return $this->fecha_fin;
    }

    /**
     * Set rep
     *
     * @param string $rep
     * @return DatosInstitucion
     */
    public function setRep($rep)
    {
        $this->rep = $rep;

        return $this;
    }

    /**
     * Get rep
     *
     * @return string 
     */
    public function getRep()
    {
        return $this->rep;
    }
}
